package com.businessreviews.model.annotation;

/**
 * 敏感数据脱敏类型枚举
 * <p>
 * 定义不同类型敏感数据的脱敏策略。
 * </p>
 *
 * @author businessreviews
 */
public enum SensitiveType {

    /**
     * 手机号脱敏
     * 规则: 保留前3位和后4位，中间4位替换为****
     * 示例: 138****1234
     */
    PHONE,

    /**
     * 邮箱脱敏
     * 规则: @前只显示前3个字符，其余替换为***
     * 示例: abc***@example.com
     */
    EMAIL,

    /**
     * 身份证号脱敏
     * 规则: 保留前6位和后4位，中间替换为****
     * 示例: 330106****1234
     */
    ID_CARD,

    /**
     * 银行卡号脱敏
     * 规则: 保留前4位和后4位，中间替换为****
     * 示例: 6222****8888
     */
    BANK_CARD,

    /**
     * 姓名脱敏
     * 规则: 只显示第一个字符，其余替换为*
     * 示例: 张**
     */
    NAME,

    /**
     * 地址脱敏
     * 规则: 只显示前6个字符，其余替换为***
     * 示例: 浙江省杭州***
     */
    ADDRESS,

    /**
     * 自定义脱敏
     * 规则: 全部替换为***
     */
    CUSTOM
}
